func ParseProjectDir(filePath string, projectDir string) (string, error) {
	// Check for project directory placeholders in the filePath
	patterns := []string{
		"$projectdir",
		"$projectDir",
		"${ProjectDir}",
		"$ProjectDir",
		"$project",
		"$Project",
		"${projectDir}",
		"${projectdir}",
		"${Project}",
		"${project}",
	}

	result := filePath

	for _, pattern := range patterns {
		if strings.HasPrefix(filePath, pattern) {
			result = strings.Replace(result, pattern, projectDir, 1)
			break
		}
	}

	// If result is a relative path, convert it to an absolute path
	if !filepath.IsAbs(result) {
		if absPath, err := filepath.Abs(result); err != nil {
			return "", fmt.Errorf("failed to convert path to absolute path: %w", err)
		} else {
			result = absPath
		}
	}

	return result, nil
}

func UnmarshalConfig(filePath string, v any) error {
	if content, err := os.ReadFile(filePath); err != nil {
		return err
	} else {
		switch filepath.Ext(filePath) {
		case ".json":
			return json.Unmarshal(content, v)
		case ".yaml", ".yml":
			return yaml.Unmarshal(content, v)
		default:
			return fmt.Errorf("unsupported file extension: %s", filepath.Ext(filePath))
		}
	}
}

func LoadConfig(filePath string) (APIConfig, error) {
	var config APIConfig

	if err := UnmarshalConfig(filePath, &config); err != nil {
		return APIConfig{}, fmt.Errorf("failed to parse config file: %w", err)
	}

	return config, nil
}

func LoadRtConfig() (RTConfig, string, error) {
	configPath := ""

	if len(os.Args) > 1 {
		if fileInfo, err := os.Stat(os.Args[1]); err == nil && !fileInfo.IsDir() {
			configPath = os.Args[1]
		}
	}

	if configPath == "" {
		// 在当前目录下，依次寻找 .rt.json .rt.yaml .rt.yml
		searchFiles := []string{"./.rt.json", "./.rt.yaml", "./.rt.yml"}
		for _, file := range searchFiles {
			if fileInfo, err := os.Stat(file); err == nil && !fileInfo.IsDir() {
				configPath = file
				break
			}
		}
	}

	if !filepath.IsAbs(configPath) {
		if absPath, err := filepath.Abs(configPath); err != nil {
			return RTConfig{}, "", fmt.Errorf("failed to convert config path to absolute path: %v", err)
		} else {
			configPath = absPath
		}
	}

	var config RTConfig

	if err := UnmarshalConfig(configPath, &config); err != nil {
		return RTConfig{}, "", fmt.Errorf("failed to parse config file: %w", err)
	}

	projectDir := filepath.Dir(configPath)

	for i, output := range config.Outputs {
		var err error
		config.Outputs[i].Dir, err = ParseProjectDir(output.Dir, projectDir)
		if err != nil {
			return RTConfig{}, "", fmt.Errorf("failed to parse output dir: %w", err)
		}

		if !filepath.IsAbs(config.Outputs[i].Dir) {
			config.Outputs[i].Dir = filepath.Join(projectDir, config.Outputs[i].Dir)
		}
	}

	return config, configPath, nil
}

func Output() error {
	rtConfig, rtConfigPath, err := LoadRtConfig()
	if err != nil {
		log.Panicf("Failed to load config: %v", err)
	}

	apiVersions := []string{"rt.api.v1"}
	dbVersions := []string{"rt.db.v1"}
	projectDir := filepath.Dir(rtConfigPath)
	log.Printf("rt: project dir: %s\n", projectDir)
	log.Printf("rt: config file: %s\n", rtConfigPath)

	for _, output := range rtConfig.Outputs {
		if output.HttpEngine != "" {
			systemDir := filepath.Join(output.Dir, "_rt_system_")
			assetsFile := fmt.Sprintf(
				"assets/%s/%s/%s/%s.%s",
				output.Language,
				output.Kind,
				"_rt_system_",
				engineMap[output.HttpEngine],
				languageTailMap[output.Language],
			)

			if err := os.RemoveAll(systemDir); err != nil {
				log.Fatalf("failed to remove system dir: %v", err)
			} else if err := os.Mkdir(systemDir, 0755); err != nil {
				log.Fatalf("failed to create system dir: %v", err)
			} else if content, err := assets.ReadFile(assetsFile); err != nil {
				log.Fatalf("failed to read assets file: %v", err)
			} else if err := os.WriteFile(filepath.Join(systemDir, "http.go"), content, 0644); err != nil {
				log.Fatalf("failed to write assets file: %v", err)
			}
		}

		walkErr := filepath.Walk(projectDir, func(path string, info os.FileInfo, err error) error {
			if err != nil {
				return err
			}
			if info.IsDir() {
				return nil
			}

			var header struct {
				Version string `json:"version"`
			}

			switch filepath.Ext(path) {
			case ".json", ".yaml", ".yml":
				if err := UnmarshalConfig(path, &header); err != nil {
					return nil // Not a rt config file, just ignore.  continue walking
				} else if slices.Contains(apiVersions, header.Version) {
					return OutputFile(ConfigKindAPI, rtConfig, rtConfigPath, path, output) // output file
				} else if slices.Contains(dbVersions, header.Version) {
					return OutputFile(ConfigKindDB, rtConfig, rtConfigPath, path, output) // output file
				} else {
					return nil
				}
			default:
				return nil
			}
		})

		if walkErr != nil {
			return fmt.Errorf("error walking project directory: %w", walkErr)
		}
	}

	return nil
}

func OutputFile(kind ConfigKind, rtConfig RTConfig, rtConfigPath string, buildConfigPath string, output RTOutputConfig) error {
	if buildConfig, err := LoadConfig(buildConfigPath); err != nil {
		return fmt.Errorf("failed to load config file: %w", err)
	} else {
		var builder IBuilder
		buildContext := BuildContext{
			output:          output,
			rtConfig:        rtConfig,
			buildConfig:     buildConfig,
			rtConfigPath:    rtConfigPath,
			buildConfigPath: buildConfigPath,
		}

		switch output.Language {
		case "golang":
			builder = &GolangBuilder{
				BuildContext: buildContext,
			}
		case "typescript":
			builder = &TypescriptBuilder{
				BuildContext: buildContext,
			}
		default:
			return fmt.Errorf("unsupported language: %s", output.Language)
		}

		switch kind {
		case ConfigKindAPI:
			switch output.Kind {
			case "server":
				return builder.BuildAPIServer()
			case "client":
				return builder.BuildAPIClient()
			default:
				return fmt.Errorf("unsupported kind: %s", output.Kind)
			}
		case ConfigKindDB:
			switch output.Kind {
			case "server":
				return builder.BuildDBServer()
			case "client":
				return builder.BuildDBClient()
			default:
				return fmt.Errorf("unsupported kind: %s", output.Kind)
			}
		default:
			return fmt.Errorf("unsupported kind: %s", output.Kind)
		}
	}
}
